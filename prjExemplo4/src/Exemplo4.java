
public class Exemplo4 {

    public static void main(String[] args) {
        int cont;
        
        System.out.println("    TABUADA DO 7\n");
        cont = 1;
        
        while(cont < 11)
        {
            System.out.println("7 * " + cont + " = " + cont * 7);
            cont++;
        }
    }
}
