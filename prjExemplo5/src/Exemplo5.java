
public class Exemplo5 {

    public static void main(String[] args) {
        int cont;
        
        System.out.println("    TABUADA DO 7\n");
        cont = 1;
        
        do{
            System.out.println("7 * " + cont + " = " + cont * 7);
            cont++;
        }while(cont <= 10);
    }
}
